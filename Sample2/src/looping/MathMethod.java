package looping;

public class MathMethod {
	public static void main(String[]args) {
		System.out.println(Math.abs(-60));
		System.out.println(Math.round(78.5));
		System.out.println(Math.ceil(78.4));
		System.out.println(Math.floor(78.9));
		System.out.println(Math.sqrt(64));
		System.out.println(Math.round(Math.random()*10000));
	}
}
